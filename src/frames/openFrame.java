    private void setAll(String selectedFile) throws Exception{
        this.informer = new Informer (selectedFile);
        informer.setSimpleKeys(new String [] {"greetings", "rules", "questions"});
        informer.setSimpleValues(new String [] {"evaluateImmediately", "tellPoints", "numOfQuestions"});
        informer.setTypesOfSimpleValues(new String [] {"bool", "bool", "int"});
        informer.setDoubleKeys(new String [][] {{"reactions", "positive", "negative"}, {"commands", "stop", "volume_up", "volume_down"}, {"points"}});
        informer.checkAll();
    }
        
    private void startNewApp(String selectedFile, boolean newF) throws Exception{
        if (newF)
            setAll(selectedFile);
        MainFrame mFrame = new MainFrame(selectedFile, informer);
        mFrame.setVisible(true);
        mFrame.pack();
        this.dispose();
    }
    private void jBCreateFileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBCreateFileMouseClicked
        String fileName = JOptionPane.showInputDialog("Enter file name") + ".json";
        File selectedFile = null;
        int res = JOptionPane.showConfirmDialog(rootPane, "Create file " + fileName);
        if (res == 0){
            try {
          selectedFile = new File(fileName);
          if (selectedFile.createNewFile()) {
              JOptionPane.showMessageDialog(rootPane, "File created: " + selectedFile.getName());
          } else {
              JOptionPane.showMessageDialog(rootPane, "File already exists.");
          }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(rootPane, "An error occurred.");
            }
        }
        
        if (selectedFile != null){
            try {
                startNewApp(selectedFile.getAbsolutePath(), true);
            } catch (Exception ex) {
                Logger.getLogger(openFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBCreateFileMouseClicked


