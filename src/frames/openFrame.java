    @Override
    protected void processWindowEvent(WindowEvent e){
        if (e.getID() == WindowEvent.WINDOW_CLOSING){
            if (informer == null)
                System.exit(0);
            boolean exit = informer.checkIfNoEmptyFields();
            if (exit)
                System.exit(0);
        }
   }
    
    private void jBChooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBChooseFileActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            
            if (selectedFile != null)
                try {
                    startNewApp(selectedFile.getAbsolutePath(), false);
            } catch (Exception ex) {
                Logger.getLogger(openFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }  
    }//GEN-LAST:event_jBChooseFileActionPerformed

